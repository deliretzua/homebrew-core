class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://kcachegrind.github.io/"
  url "https://download.kde.org/stable/release-service/22.04.2/src/kcachegrind-22.04.2.tar.xz"
  sha256 "d685bc4d6ea53dfc4ecdb307e3c3cade6228467b93c400b42fd1fd0b924841b6"
  license "GPL-2.0-or-later"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_monterey: "9fc380a3751609ac4c7fcea0048d5f78ffadaddbbe88117ffdf08bf358322fae"
    sha256 cellar: :any,                 arm64_big_sur:  "2659b8bc9d9cc5f052a49c45d2a4a4e03b35ad04870a89ecf35b3cf00194099a"
    sha256 cellar: :any,                 monterey:       "cc03a20510a1a0aa1e7ad4e21c9c437eb84de542f21b870835ac88c16e2e3843"
    sha256 cellar: :any,                 big_sur:        "9744e34d4725867822983c6377a5cc66043cadad850bdf8841b7bcec61473fb9"
    sha256 cellar: :any,                 catalina:       "133fc3f7d7c605b7672f9139f7bc4e960b1a28be35f8db630caa7292888b73c5"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "2db6165642f7dea5b3533cf5f1a0fe648cc504fb687c7d4878d56b21df3f9fa1"
  end

  depends_on "graphviz"
  depends_on "qt@5"

  on_linux do
    depends_on "gcc"
  end

  fails_with gcc: "5"

  def install
    args = ["-config", "release", "-spec"]
    os = OS.mac? ? "macx" : OS.kernel_name.downcase
    compiler = ENV.compiler.to_s.start_with?("gcc") ? "g++" : ENV.compiler
    arch = Hardware::CPU.intel? ? "" : "-#{Hardware::CPU.arch}"
    args << "#{os}-#{compiler}#{arch}"

    system Formula["qt@5"].opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end
