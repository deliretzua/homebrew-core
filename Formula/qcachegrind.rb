class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://kcachegrind.github.io/"
  url "https://download.kde.org/stable/release-service/22.04.1/src/kcachegrind-22.04.1.tar.xz"
  sha256 "5af150fc24f36f67f4c86ccb7a5a85067619685c4639fa905195136e84a7785b"
  license "GPL-2.0-or-later"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_monterey: "8648e2858987a3a841da544389a89ea967d2df9765a6df3051cacc58e5079a15"
    sha256 cellar: :any,                 arm64_big_sur:  "7d40b47e575990417e87d214c165a5e0a14c8ede6750326e6c53603232b2365d"
    sha256 cellar: :any,                 monterey:       "1026b6d2d7d86239b4c2240215485961e74dc33a93ab5b4025b78f832a4e1f55"
    sha256 cellar: :any,                 big_sur:        "2e46007474e583263d9a92739405192c6ca346966a14788c351e3361a54125f6"
    sha256 cellar: :any,                 catalina:       "c3efe6731b2bce6e5f13634265d3452aa9f3816f9e03b984bb5f86e33bff654d"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "7f7a629378d6c33a695a4a6164bf8c8e5533de4ca05dc9b6318a8aa699a24d24"
  end

  depends_on "graphviz"
  depends_on "qt@5"

  on_linux do
    depends_on "gcc"
  end

  fails_with gcc: "5"

  def install
    args = ["-config", "release", "-spec"]
    os = OS.mac? ? "macx" : OS.kernel_name.downcase
    compiler = ENV.compiler.to_s.start_with?("gcc") ? "g++" : ENV.compiler
    arch = Hardware::CPU.intel? ? "" : "-#{Hardware::CPU.arch}"
    args << "#{os}-#{compiler}#{arch}"

    system Formula["qt@5"].opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end
