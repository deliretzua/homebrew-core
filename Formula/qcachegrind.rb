class Qcachegrind < Formula
  desc "Visualize data generated by Cachegrind and Calltree"
  homepage "https://kcachegrind.github.io/"
  url "https://download.kde.org/stable/release-service/21.12.3/src/kcachegrind-21.12.3.tar.xz"
  sha256 "ddcf3722211d47c90616528607ebc4573835faa52a57e2e9d6763968b9975ab3"
  license "GPL-2.0-or-later"

  # We don't match versions like 19.07.80 or 19.07.90 where the patch number
  # is 80+ (beta) or 90+ (RC), as these aren't stable releases.
  livecheck do
    url "https://download.kde.org/stable/release-service/"
    regex(%r{href=.*?v?(\d+\.\d+\.(?:(?![89]\d)\d+)(?:\.\d+)*)/?["' >]}i)
  end

  bottle do
    sha256 cellar: :any,                 arm64_monterey: "56c2caefd1ae67a1fb534736b5cddc51114478fe58b5d8d37a1deb9e2572326e"
    sha256 cellar: :any,                 arm64_big_sur:  "74e005c3dbf58b1fbd1c100c90f16ba6aa27ad68fe9933f04a8aaf195b298be5"
    sha256 cellar: :any,                 monterey:       "3826f921dfc7263d3bfcef5464339b0ef31e3e19f252a5f6128ea5bd4087793c"
    sha256 cellar: :any,                 big_sur:        "ed35eed4a23eb962a878a4cfbe91dc2f51c2acda094c1dacf06eca7968150c04"
    sha256 cellar: :any,                 catalina:       "c5b0af9345d17847649237617ac99d88252dd308f84810a3e1f08d4526ac8796"
    sha256 cellar: :any_skip_relocation, x86_64_linux:   "b0e6590de2d4aa248432641e37367ad31acd5e65958b958594a58db474c4cfed"
  end

  depends_on "graphviz"
  depends_on "qt@5"

  on_linux do
    depends_on "gcc"
  end

  fails_with gcc: "5"

  def install
    args = ["-config", "release", "-spec"]
    os = OS.mac? ? "macx" : OS.kernel_name.downcase
    compiler = ENV.compiler.to_s.start_with?("gcc") ? "g++" : ENV.compiler
    arch = Hardware::CPU.intel? ? "" : "-#{Hardware::CPU.arch}"
    args << "#{os}-#{compiler}#{arch}"

    system Formula["qt@5"].opt_bin/"qmake", *args
    system "make"

    if OS.mac?
      prefix.install "qcachegrind/qcachegrind.app"
      bin.install_symlink prefix/"qcachegrind.app/Contents/MacOS/qcachegrind"
    else
      bin.install "qcachegrind/qcachegrind"
    end
  end
end
